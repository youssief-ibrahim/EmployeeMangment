using EmployeeMangment.Data;
using EmployeeMangment.Reposatory;
using Microsoft.EntityFrameworkCore;

namespace EmployeeMangment
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();
            builder.Services.AddDbContext<ApplecationDbContext>(options =>
            {
                options.UseSqlServer(builder.Configuration.GetConnectionString("defultconnection"));
            });
            builder.Services.AddScoped(typeof(IGnericReposatory<>), typeof(GnericReposatory<>));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Employee}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
